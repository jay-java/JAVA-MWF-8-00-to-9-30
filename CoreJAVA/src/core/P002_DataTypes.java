package core;

public class P002_DataTypes {
	public static void main(String[] args) {
		// primitive -
		// byte,short,int,long - whole numbers
		byte b = 127; // 1
		short s = 12; // 2
		int i = 12; // 4
		System.out.println(((Object)i).getClass().getSimpleName());
		long l = 12; // 8

		// float,double - number with decimals
		float f = 346.456f; // 4
		double d = 345.345; // 8

		// char - only for single character
		char c = 'g';

		// boolean - true/false
		boolean flag = false;

		
		//non-primitive - array,String,class

	}
}
